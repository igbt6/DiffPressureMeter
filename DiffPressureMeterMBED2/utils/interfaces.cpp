//-----------------------------------------------------------------------------
// SPI__1
SPI__1::SPI__1()
    : spi(SPI__1::mosi, SPI__1::miso, SPI__1::sclk)
{
    spi.format(8,3);
    spi.frequency(12000000); // 40MHz clock
}


SPI& SPI__1::handle()
{
    return spi;
}
//-----------------------------------------------------------------------------

//-----------------------------------------------------------------------------
// I2C__1
I2C__1::I2C__1()
    : i2c(I2C__1::sda, I2C__1::scl)
{
    i2c.frequency(400000); // 400KHz
}


I2C& I2C__1::handle()
{
    return i2c;
}

